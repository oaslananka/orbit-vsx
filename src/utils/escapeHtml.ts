/** Escapes HTML special characters for safe interpolation in HTML templates. */
export function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

/** Serializes data without allowing it to terminate an inline script element. */
export function serializeJsonForInlineScript(value: unknown): string {
  return (JSON.stringify(value) ?? 'null')
    .replace(/&/g, '\\u0026')
    .replace(/</g, '\\u003c')
    .replace(/>/g, '\\u003e')
    .replace(/\u2028/g, '\\u2028')
    .replace(/\u2029/g, '\\u2029');
}
